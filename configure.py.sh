PYTHON_BIN=""

# Try python3 first, then python.
which python3 >/dev/null 2>&1 && PYTHON_BIN="python3"
[ -z "$PYTHON_BIN" ] && which python >/dev/null 2>&1 && PYTHON_BIN="python"

if [ -z "$PYTHON_BIN" ]; then
    echo "Python is not found."
    echo ""
    echo "Python 3 is required in order to build VirtualBox."
    echo "Please install Python 3 and ensure it is in your PATH."
    exit 1
fi

exec "$PYTHON_BIN" configure.py "$@"
exit $?
